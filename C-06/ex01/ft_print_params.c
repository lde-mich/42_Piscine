#include<unistd.h>

int	main(int ag, char **av)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (j < ag)
	{
		i = 0;
		while (av[j][i] != '\0')
		{
			write (1, &av[j][i], 1);
			i++;
		}
		write (1, "\n", 1);
		j++;
	}
	return (0);
}
