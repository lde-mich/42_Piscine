#include<unistd.h>

int	main(int ag, char **av)
{
	int	i;
	int	j;

	j = ag - 1;
	while (j > 0)
	{
		i = 0;
		while (av[j][i] != '\0')
		{
			write (1, &av[j][i], 1);
			i++;
		}
		write (1, "\n", 1);
		j--;
	}
	return (0);
}
