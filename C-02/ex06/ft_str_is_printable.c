int	ft_str_is_printable(char *str)
{
	int	i;
	int	temp;

	i = 0;
	temp = 1;
	while (str[i] != '\0')
	{
		if (str[i] < ' ' || str[i] > '~')
			temp = 0;
		i++;
	}
	return (temp);
}
