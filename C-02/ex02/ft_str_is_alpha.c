int	ft_str_is_alpha(char *str)
{
	int	i;
	int	temp;

	i = 0;
	temp = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'z')
		{
			temp = 0;
			if (str[i] >= '[' || str[i] <= '`')
			{
				temp = 0;
			}
		}
		else if (!(str[i] >= 'a') && !(str[i] <= 'z'))
			temp = 0;
		i++;
	}
	return (temp);
}
