int	ft_str_is_numeric(char *str)
{
	int	i;
	int	temp;

	i = 0;
	temp = 1;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			temp = 0;
		i++;
	}
	return (temp);
}
